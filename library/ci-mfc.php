                <p class="methodText">
                    Let us assume that a Home Repairs company wanted their employees to have a Mobile Application when they went to service their customers’s homes. This application would help them to bill their customers, get details of the customers such as phone number and address, and also help them to submit the report of the service completion. 
                </p>
                <p class="methodText">
                For this Mobile App to provide the Service Man the required functionality, it would need to be securely integrated with their Cloud-Based Salesforce.com CRM. This could be done easily using the IBM’s CastIron technology as follows, 
                </p>
                <ul>
                    <li>The User launches the “Home Servicing App” on his mobile device </li>
                    <li>He goes to the Customer Search Screen and submits the Customer’s name</li>
                    <li>The Mobile App takes this data and sends a request to the MobileFirst Server</li>
                    <li>The CastIron Adapter then takes this data and sends a HTTP Request to the CastIron Cloud (or) Appliance</li>
                    <li>CastIron in turn communicates with SFDC and returns the required customer information to the CastIron adapter as a XML/HTTP response</li>
                    <li>The CastIron adapter then takes this XML response and sends the Mobile App the data back in JSON format</li>
                    <li>The DOJO UI framework then takes this JSON data and exposes it in a readable table to the user on his Mobile Device</li>
              </ul>
                <p class="methodText">
                    With IBM MobileFirst and CastIron, the Home Servicing Company was able to easily expose its enterprises back-end resources to its field personnel through mobile devices, in turn increasing their customer’s experience and employee productivity. 
