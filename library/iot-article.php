                    <h5 class="heavy noMargin">
                  IoT for a Cold Storage Facility
                 </h5>
                 <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                    <p class="methodText">A Cold Storage facility is used for storing refrigerated items which need to be maintained at a particular temperature and humidity. Produce, Ice Creams and Beverages can all be lost if the facilities is not monitored properly. <strong>So how can a cold storage facility manager innovate with technology?</strong></p>
                     <p class="methodText">Our team came up with a simple solution, which enables the facility to transmit data back to the manager to ensure that he is on top of its maintenance needs. With the solution the manager can,
                     <ul>
                         <li>Check Temperature and Humidity related data from anywhere through Mobile/Web Apps</li>
                          <li>Get notified of changes in conditions where ever he is in the world</li>
                           <li>Take action and control the thermostat over the Internet from virtually anywhere</li>
                            <li>Store data in a NoSQL DB for further analytics and dash boarding</li>
                             <li>Apply Predictive Analytics to the collected sensor data to predict maintenance needs of the cold storage facility</li>
                     </ul>
                     </p>
                     <img class="img-responsive" src="../images/library/iot-cloud.png">
                     <br>
                      <h5 class="heavy noMargin">
                Flow of Data
                 </h5>
                  <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                    <p class="methodText">The starting point of the data would be the sensors, with a Mobile App, a NoSQL DB (or) a Twitter Feed being the end result of the data flow.
                    <ul>
                        <li>The <strong>TI Sensor Tag</strong> acts as the Temperature/Humidity Sensor inside the Cold Storage Facility Storage</li>
                        <li>The <strong>Raspberry PI</strong> acts as a gateway to the Internet and collects data from the sensors via <strong>Low Energy Bluetooth</strong></li>
                         <li>The Raspberry PI is registered with <strong>IBM Bluemix</strong> using its MAC ID and transmits all sensor data to the <font color="#00aae7"><strong>Internet of Things Foundation Service</strong></font> </li>
                        <li>The data then flows in two directions as mention below</li>
                    </ul>
                    </p>
                    
                    
                 <h6 class=" heavynoMargin">
                 Notifications for Facility Manager
                 </h6>
                 <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                <p class="methodText"> One of the most important use cases of the sensor data would be to inform the Facility Manager of any anomalies in the facilities conditions. This is achieved by taking the data into <strong>NodeRed</strong> and creating an orchestration to test the data and notify the manager.</p>
                <ul>
                <li> NodeRed takes the data and executes a defined test on the temperature and humidity data coming from the sensors</li>
                <li>When an anomaly is detected the data is passed to a Bluemix Service such as <strong>Twilio</strong> (or) <strong>Twitter</strong></li>
                <li>These services then take the data and send a defined message to the Facility Manager about the anomaly</li>
                <li>The Facility Manager can then see this as a Push Notification, Email, Twitter Feed (or) Twilio Message and act accordingly</li>
                <li>Simultaneously the notification and anomaly data is also sent over to a NoSQL DB such as <strong>Cloudant</strong> for Persistence</li>
               </ul>
               
                 <h6 class=" heavynoMargin">
                Dash boarding, Persistence and Analytics
                 </h6>
                 <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                <p class="methodText"> Even though anomaly notification is vital, historical sensor data is equally important for dash boarding and analytics. The second use case would be to take this sensor data and persist it into a data store to enable the Facility Manager to do the following,</p>
                <ul>
                <li> Query the NoSQL DB and check historical data and anomalies</li>
                <li>Apply Analytics to the data to check for patterns and predict outcomes</li>
                <li>Dashboard real-time sensor data for monitoring of the facility</li>
                
               </ul></p>
               <p class="methodText">Using Bluemix and its Node.JS Runtime, developers can create Mobile Apps that Facility Managers can use to check for notifications, real-time data feeds and also take action by controlling facility equipment.</p>
