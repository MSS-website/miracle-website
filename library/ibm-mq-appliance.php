                 <h5 class="heavy noMargin">
                     Appliance = Advantage
                 </h5>
                 <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                 <p class="methodText">
                     With MQ being used in a software based approach for so many implementations, the whole idea of the appliance prompts questions regarding the advantages and value of moving to an appliance based deployment model for messaging.
                
                 <strong>Some of the prime advantages and deployment options for the new MQ Appliance are, </strong>
                 <ul>
                     <li>With a dedicated appliance your messaging footprint is simpler and smaller</li>
                     <li>Easier administration with separated roles and MQ Console Tool(Browser Based)</li>
                     <li>Pre-Optimized Hardware for highest and optimum performance</li>
                     <li>Certified firmware updates from IBM for both MQ and firmware</li>
                     <li>Lower data center costs, space usage and power requirements</li>
                     <li>High Availability provided with the appliance out-of-the-box</li>
                 </ul>
                  </p>
                 <h5 class="heavy noMargin">
                   Use Existing Deployments
                 </h5>
                 <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                    <p class="methodText">Another question that is bound to pop up is the usage of your existing MQ deployments along with new appliance based Queue Manager Deployments.<strong> Is it possible to use our new MQ Appliance along with existing MQ networks and clusters?</strong></p>
                     <p class="methodText">The answer is YES! 
                    </p>
                     <p class="methodText">
                         The firmware of the MQ Appliance includes MQ v8.0 capabilities and thus allows the appliance to participate in existing MQ networks and clusters. Since applications cannot be run on the appliance, all existing MQ applications connect to the appliance as clients with no code changes required. 
                      </p> 
                      
                       <h5 class="heavy noMargin">
                  The Differences
                 </h5>
                 <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                    <p class="methodText">
                        Even though the MQ Appliance comes with MQ v8 on it, the deployment capabilities of the two versions varies vastly. Some of the major differences between software-based MQ and the appliance-based are, 
                   
                    <ul>
                        <li>The Appliance uses a Hub-Based pattern, which means that applications must connect as remote clients</li>
                        <li>No user exits can be run on the appliance and monitoring must be done with remote monitoring agents</li>
                        <li>Shared file systems and disks are not required for providing MQ Appliance</li>
                        <li>All firmware updates are done through single installable fix packs which are certified by IBM</li>
                        <li>The CLI of the appliance should not be used as a general purpose shell</li>
                    </ul>
                     </p>
                     <h5 class="heavy noMargin">
                  Appliance Security
                 </h5>
                 <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                    <p class="methodText">
                        The MQ Appliance comes with advanced security features, AMS and also a tamper-proof physical appliance, which ensures that your messaging components stay secure for your enterprise. Some of the new key security aspects of the appliance are, 
                   
                    <ul>
                        <li>The appliance supports SSL/TSL connectivity and also gives you the option to upload existing certificates for authentication</li>
                        <li>You can manage your users both locally(Small Group) and using an external LDAP repository for larger user groups(With LDAP users can become global across appliances)</li>
                        <li>With MQ Internet Pass-Thru you can enable proxy based tunneling for appliance connectivity</li>
                        <li>With the MQ Appliance you can now separate Messaging Administrators and Appliance Administrators</li>
                    </ul>
                     </p>
                     <h5 class="heavy noMargin">
               Deployment Use Cases
                 </h5>
                 <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                  <p class="methodText">
                      The deployment options and use cases with the new MQ Appliance are limitless, but some of the most important and possible use cases within your messaging infrastructure would be, 
                      </p>
                      <h6 class="heavy">
                          A Messaging Hub
                      </h6>
                       <p class="methodText">
                           Lack of standardization within your messaging infrastructure makes rapid changes, migrations and upgrades a complex activity. The MQ Appliance can help you deploy a simpler and smaller footprint in a standardized deployment model, which in turns make sure that your messaging is contained in a Highly Available Messaging Hub. 
                           </p>
                           <h6 class="heavy">
                          For Business Partners
                      </h6>
                       <p class="methodText">
                           In today’s demanding market-place it is important for an enterprise to be connected to its business partners and this becomes complicated when the partners lack MQ skills and support personnel. This is where the MQ Appliance can be used to connect you with your business partners in a simple, secure and reliable manner. 
                           </p>
                           <h6 class="heavy">
                        Remote Deployment
                      </h6>
                        <p class="methodText">
                            Remotely deploying messaging options used to need strong IT Administration Teams, and difficulties with failover support. This all changes with the remote deployment options of the new MQ M2000. The MQ Appliance makes the deployment, operation and maintenance of your remote messaging in retail stores, factories, warehouses and various other remote locations. You can have a remote pre-configured MQ Appliance up-and-running with simple setup steps and plug-in options (or) remotely configure the appliance. 
                            </p>
