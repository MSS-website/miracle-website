                    <p class="methodText">
                       As today’s enterprise becomes much more connected, messaging plays a crucial role in ensuring that your connected architecture is flexible, simple and reliable. After years of dominance in the Messaging and Connectivity space, this February IBM announced the arrival of MQ v8.0 in the form factor of a physical appliance. The MQ Appliance brings great aspects such as simplicity, scalability, security, rapid and remote deployment, and also high availability through paired appliances.
                       </p>
                    <strong>Some of the new features of the MQ Appliance 2000 are, </strong>
                   
                    <ul>
                        <li>The hardware and OS of the MQ Appliance is based on the proven IBM DataPower Appliance</li>
                        <li>Firmware includes MQ v8.0 and has the ability to utilize existing MQ networks and clusters</li>
                        <li>Built in high availability using paired appliance with automatic queue manager failover</li>
                        <li>Advanced security options with LDAP-based authentication and authorization with <strong>MQ AMS</strong></li>
                        <li>Administration concepts are very similar to existing MQ Software Administration</li>
                        <li>Premium performance right out-of-the-box with a very simple deployment procedure</li>
                    </ul>
                     <h5 class="heavy noMargin">The MQ M2000 A/B</h5>
                      <div class="row">
                        <div class="col-sm-3">
                            <hr>
                        </div><!-- This is the divider line between the heading and the content -->
                    </div>
                     <p class="methodText">
                         The new MQ Appliance comes in two unique models, the M2000 A and the M2000 B. Both the models are based on the same physical appliance, with the major difference being the number of processor cores, which reduces the price and performance of the M2000 B model.
                     </p>
                     <strong>Some of the features of the two models are, </strong>
                </div>
